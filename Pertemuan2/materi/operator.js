// Arithmetic Operators
let a = 10;
let b = 5;
console.log('Arithmetic Operators:');
console.log('a + b =', a + b); // Addition
console.log('a - b =', a - b); // Subtraction
console.log('a * b =', a * b); // Multiplication
console.log('a / b =', a / b); // Division
console.log('a % b =', a % b); // Modulus
console.log('a ** b =', a ** b); // Exponentiation

// Assignment Operators
let c = 10;
console.log('\nAssignment Operators:');
c += 5; // c = c + 5
console.log('c += 5:', c);
c -= 3; // c = c - 3
console.log('c -= 3:', c);
c *= 2; // c = c * 2
console.log('c *= 2:', c);
c /= 4; // c = c / 4
console.log('c /= 4:', c);
c %= 3; // c = c % 3
console.log('c %= 3:', c);

// Comparison Operators
console.log('\nComparison Operators:');
console.log('a == b:', a == b); // Equal to
console.log('a != b:', a != b); // Not equal to
console.log('a > b:', a > b); // Greater than
console.log('a < b:', a < b); // Less than
console.log('a >= b:', a >= b); // Greater than or equal to
console.log('a <= b:', a <= b); // Less than or equal to

// Logical Operators
console.log('\nLogical Operators:');
console.log('true && false:', true && false); // AND
console.log('true || false:', true || false); // OR
console.log('!true:', !true); // NOT

// String Operators
console.log('\nString Operators:');
let str1 = 'Hello';
let str2 = 'World';
console.log('str1 + str2:', str1 + ' ' + str2); // Concatenation